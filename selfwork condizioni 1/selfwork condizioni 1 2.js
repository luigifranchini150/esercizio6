let voto = 18;

if(voto <= 18){
    console.log(`insufficente`);
}else if(voto > 18 && voto <= 21){
    console.log(`sufficente`); 
}else if(voto > 21 && voto <= 24){
    console.log(`buono`); 
}else if(voto > 24 && voto <= 27){
    console.log(`ottimo`); 
}else if(voto > 27 && voto <= 29){
    console.log(`distinto`); 
}else if(voto == 30){
    console.log(`eccellente`); 
}














